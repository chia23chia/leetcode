class Solution {
public:
    bool arrayStringsAreEqual(vector<string>& word1, vector<string>& word2) {
        bool ans ;
        string t1 ;
        string t2 ;
        for(int i = 0 ; i < word1.size() ; i++)
            t1 = t1 + word1[i] ;
        for(int i = 0 ; i < word2.size() ; i++)
            t2 = t2 + word2[i] ;
        if(t1 == t2)
            ans = true ;
        else 
            ans = false ;
        /*for(int i = 0 ; i < t1.size() ; i++)
            if(t1[i] != t2[i])
            {
                ans = false ;
                break ;
            }*/
        
        return ans ;
    }
};
